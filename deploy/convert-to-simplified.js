// Function to replace traditional characters with simplified
function convertToSimplified(text) {
  let result = text;
  
  // Sort keys by length in descending order to replace longer phrases first
  const sortedKeys = Object.keys(traditionalToSimplified).sort((a, b) => b.length - a.length);
  
  for (const traditional of sortedKeys) {
    const simplified = traditionalToSimplified[traditional];
    // Use global replacement to replace all occurrences
    const regex = new RegExp(traditional, 'g');
    result = result.replace(regex, simplified);
  }
  
  return result;
}

// Function to process a file
function processFile(filePath) {
  try {
    const content = fs.readFileSync(filePath, 'utf8');
    const convertedContent = convertToSimplified(content);
    
    if (content !== convertedContent) {
      fs.writeFileSync(filePath, convertedContent, 'utf8');
      console.log(`‚úÖ Converted: ${filePath}`);
      return true;
    } else {
      console.log(`‚è≠Ô∏è  No changes needed: ${filePath}`);
      return false;
    }
  } catch (error) {
    console.error(`‚ùå Error processing ${filePath}:`, error.message);
    return false;
  }
}

// Function to recursively find and process files
function processDirectory(directory, extensions) {
  let totalFiles = 0;
  let convertedFiles = 0;
  
  function walkDir(dir) {
    const files = fs.readdirSync(dir);
    
    for (const file of files) {
      const filePath = path.join(dir, file);
      const stat = fs.statSync(filePath);
      
      if (stat.isDirectory()) {
        // Skip node_modules and other common directories
        if (!['node_modules', '.git', 'dist', 'build'].includes(file)) {
          walkDir(filePath);
        }
      } else if (stat.isFile()) {
        const ext = path.extname(file).toLowerCase();
        if (extensions.includes(ext)) {
          totalFiles++;
          if (processFile(filePath)) {
            convertedFiles++;
          }
        }
      }
    }
  }
  
  walkDir(directory);
  return { totalFiles, convertedFiles };
}

